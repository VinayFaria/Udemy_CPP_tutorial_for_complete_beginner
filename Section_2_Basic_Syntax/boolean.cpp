#include <iostream>
using namespace std;

int main() {
    bool bvalue = true; // The bool type can have values true and false.
    cout << bvalue << endl;

    bvalue = 1; // any number other than 0 is also considered as true
    cout << bvalue << endl;

    bvalue = 45;
    cout << bvalue << endl;

    bvalue = -45;
    cout << bvalue << endl;

    bvalue = false;
    cout << bvalue << endl;

    bvalue = 0;
    cout << bvalue << endl;

    return 0;
}
